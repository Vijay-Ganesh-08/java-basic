public class ternary {

    public static void main(String[] args) {
        
        int a = 10 ;
        int b = 10 ;
        System.out.println(a==b ? "True":"False");

        //ternary operator gives a value, but if wouldn't
        String result = a==b ? "True":"False";
        System.out.println(result);
    }
    
}
