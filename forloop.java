public class forloop {

    /**
     * Java for loop is a control flow statement that allows code to be executed repeatedly based on a given condition.
     * The for loop in Java provides an efficient way to iterate over a range of values, execute code multiple times,
     * or traverse arrays and collections.
     *
     * Best used when the number of iterations is known beforehand
     * or when iterating through a sequence, such as an array or a list.
     */
    public static void main(String[] args) {


        char star = '*';
        for (int i = 0;i<=10;i++){
            System.out.println(star);
        }
    }
    
}
